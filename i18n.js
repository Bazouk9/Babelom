// ── MOTEUR DE TRADUCTION ──
function babelomT(key) {
  const lang = localStorage.getItem('babelom-lang') || 'fr';
  const t = BABELOM_TRANSLATIONS[lang] || BABELOM_TRANSLATIONS['fr'];
  return t[key] || BABELOM_TRANSLATIONS['fr'][key] || key;
}

function babelomApply(lang) {
  const t = BABELOM_TRANSLATIONS[lang] || BABELOM_TRANSLATIONS['fr'];
  const rtl = ['ar', 'he'].includes(lang);
  document.documentElement.setAttribute('dir', rtl ? 'rtl' : 'ltr');
  document.documentElement.setAttribute('lang', lang);
  localStorage.setItem('babelom-lang', lang);

  // Appliquer tous les data-i18n
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // Appliquer les placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(function(el) {
    const key = el.getAttribute('data-i18n-ph');
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // Synchroniser le sélecteur de langue si présent
  const sel = document.getElementById('lang-select');
  if (sel && sel.value !== lang) sel.value = lang;
}

// Appliquer au chargement
document.addEventListener('DOMContentLoaded', function() {
  const saved = localStorage.getItem('babelom-lang') || 'fr';
  babelomApply(saved);
});
